<script>

    function  estado_form(){
        if ($('select[name=preguntas]').val()=="Sobre mi orden") {
            $("#orden").show();  
            $("#articulo").hide();
        }
        else if ($('select[name=preguntas]').val()=="Articulo de mi orden") {
            $("#articulo").show(); 
            $("#orden").hide(); 
        }else{
            $("#orden").hide();  
            $("#articulo").hide();
        }
    }
    estado_form();
    $( "#tipoBusqueda" ).change(function() {
        estado_form();
    });
</script>